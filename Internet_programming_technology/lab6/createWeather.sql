Use WEATHER

-- Создание таблицы PopulationType
CREATE TABLE ТИП_ЖИТЕЛЕЙ (
    Название NVARCHAR(255) PRIMARY KEY,
    Язык NVARCHAR(255) NOT NULL
);

-- Создание таблицы Region
CREATE TABLE РЕГИОН (
    Название NVARCHAR(255) PRIMARY KEY,
    Площадь DECIMAL(10,2) NOT NULL,
    [Тип жителей] NVARCHAR(255) FOREIGN KEY REFERENCES ТИП_ЖИТЕЛЕЙ(Название)
);

-- Создание таблицы Weather
CREATE TABLE ПОГОДА (
    id INT PRIMARY KEY,
    Регион NVARCHAR(255) FOREIGN KEY REFERENCES РЕГИОН(Название),
    Дата DATE NOT NULL,
    Температура DECIMAL(5,2) NOT NULL,
    Осадки NVARCHAR(255) NOT NULL check (Осадки in ('Снег', 'Дождь', 'Град'))
);

